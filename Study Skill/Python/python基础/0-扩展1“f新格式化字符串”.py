# 拓展——在字符串外面用f开头再加引号为格式化输出
print(f"aaa{''}")  # 外双里单为正确格式
# 注意：如果字符串中需要包含大括号，则需要使用两个大括号来转义：
print(f"{{}}")  # 输出：{}

price = 19.99
number = 123.456789


# 格式化字符串，第一个是变量，中间是冒号，右边是格式化类型
print(f"价格: {price:.2f}")        # 输出: 价格: 19.99
print(f"数字: {number:.3f}")       # 输出: 数字: 123.457
print(f"百分比: {0.25:.2%}")       # 输出: 百分比: 25.00%
print(f"科学计数: {1000000:.2e}")  # 输出: 科学计数: 1.00e+06

text = "Python"
print(f"|{text:<10}|")  # 左对齐，宽度10
print(f"|{text:>10}|")  # 右对齐，宽度10
print(f"|{text:^10}|")  # 居中对齐，宽度10
print(f"|{text:*^10}|")  # 居中对齐，用*填充

# 输出:
# |Python    |
# |    Python|
# |  Python  |
# |**Python**|

print(f"This is {number}")  # 也可直接使用更复杂的形式写，这里不做探讨了，可以在之后的学习中慢慢探索
